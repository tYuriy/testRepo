class Binomial{
    // –ó–∞–∫—Ä—ã—Ç–æ–µ –ø–æ–ª–µ-–º–∞—Å—Å–∏–≤:
    private int[] binoms;
    // –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä:
    Binomial(int n){
// –°–æ–∑–¥–∞–Ω–∏–µ –º–∞—Å—Å–∏–≤–∞:
        binoms=new int[n+1];
// –ó–Ω–∞—á–µ–Ω–∏–µ –Ω–∞—á–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –º–∞—Å—Å–∏–≤–∞:
        binoms[0]=1;
// –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –º–∞—Å—Å–∏–≤–∞:
        for(int k=1;k<=n;k++){
            binoms[k]=binoms[k-1]*(n-k+1)/k;
        }
    } // –ü—Ä–æ–¥–æ–ª–∂–µ–Ω–∏–µ –Ω–∞ —Å–ª–µ–¥—É—é—â–µ–º —Å–ª–∞–π–¥–µ!!!
    //–ü–æ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—é –±–∏–Ω–æ–º–∏–∞–ª—å–Ω—ã–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç—ã ùê∂ùëõùëò=ùëõ!ùëò!ùëõ‚àíùëò!. –ü—Ä–∏ –≤—ã—á–∏—Å–ª–µ–Ω–∏–∏ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–æ–≤ –º—ã –∏—Å—Ö–æ–¥–∏–º –∏–∑ —Ç–æ–≥–æ, —á—Ç–æ ùê∂ùëõ0=1 –∏ –¥–ª—è –≤—Å–µ—Ö ùëò=1,2,3,‚Ä¶,ùëõ –∏–º–µ–µ—Ç –º–µ—Å—Ç–æ —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–µ ùê∂ùëõùëò=ùê∂ùëõùëò‚àí1‚ãÖùëõ‚àíùëò+1ùëò.
    // –ü–µ—Ä–µ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –º–µ—Ç–æ–¥–∞ toString():
    public String toString(){
// –¢–µ–∫—Å—Ç–æ–≤–∞—è –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞:
        String txt="| ";
// –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –∫ —Ç–µ–∫—Å—Ç—É –∑–Ω–∞—á–µ–Ω–∏–π —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Å—Å–∏–≤–∞:
        for(int k=0;k<binoms.length;k++){
            txt+=binoms[k]+" | ";
        }
// –†–µ–∑—É–ª—å—Ç–∞—Ç –º–µ—Ç–æ–¥–∞:
        return txt;
    }
}
